<p><h4>Using the markdown editor is simple: </h4></p>
<p> 
First, name your new post.</p>
<p><small>Tip: <i>Pick a name that is short and to-the-point</i></small></p>
<p>You're ready to start creating!</p>
<p> Just select the type of formatting you would like from the toolbar, and begin creating content.</p>
<hr>
<h4>Previewing</h4>
<p>Just click the check button for an instant preview of your work so far. 
<hr>
<h4>Publishing</h4>
<p>To save your post, just click the "Save" button. You will see a status report on the next page. </p>
<p>To publish the new post, simply proceed to the Publish page from the next page, or click the "Publish Site" button in the top navigation.</p> 
